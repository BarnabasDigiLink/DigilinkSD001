public class Test {
    //declaring variables
    private String name;
    private String surname;
    private int age;

    //getters
    public String getName(){

        return name;
    }

    public String getSurname(){

        return surname;
    }

    public int getAge(){

        return age;
    }

    //setters
    public void setName(String newName){

        this.name = newName;
    }

    public void setSurname(String newSurname){

        this.surname = newSurname;
    }

    public void setAge(int newAge){

        this.age = newAge;
    }
}
